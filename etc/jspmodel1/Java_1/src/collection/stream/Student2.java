package collection.stream;

public class Student2 {
private String name; //지정된 것에서만 사용하기에 
private int score;

public Student2(String name, int score) {
	this.name = name;
	this.score = score;
}

public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getScore() {
	return score;
}
public void setScore(int score) {
	this.score = score;
}

}
